; Демонстрация работы с массивом
; Простая программа без сложных переходов

; Инициализация суммы
LOAD #0
STORE 100

; Суммируем элементы массива по порядку
LOAD 200        ; array[0] = 10
ADD 100         ; Добавляем к сумме
STORE 100       ; Сохраняем сумму

LOAD 201        ; array[1] = 20  
ADD 100         ; Добавляем к сумме
STORE 100       ; Сохраняем сумму

LOAD 202        ; array[2] = 30
ADD 100         ; Добавляем к сумме  
STORE 100       ; Сохраняем сумму

LOAD 203        ; array[3] = 40
ADD 100         ; Добавляем к сумме
STORE 100       ; Сохраняем сумму

LOAD 204        ; array[4] = 50
ADD 100         ; Добавляем к сумме
STORE 100       ; Сохраняем итоговую сумму

HALT

; Тестовые данные (загружаются автоматически):
; Memory[200] = 10
; Memory[201] = 20
; Memory[202] = 30
; Memory[203] = 40
; Memory[204] = 50

; Ожидаемый результат:
; Memory[100] = 150 (10+20+30+40+50)
