; Простой тест без меток для проверки GUI
; Демонстрирует основные команды

; Тест загрузки и сохранения
LOAD #42
STORE 100

; Тест арифметики  
LOAD #10
ADD #5
SUB #3
STORE 101

; Тест сравнения
LOAD 101
CMP #12
STORE 102

; Завершение
HALT

; Ожидаемые результаты:
; Memory[100] = 42
; Memory[101] = 12  (10+5-3)
; Memory[102] = 12  (результат сравнения загружается в ACC, затем сохраняется)
