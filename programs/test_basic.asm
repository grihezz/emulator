; Базовый тест всех команд процессора
; Демонстрирует работу всех типов команд и адресации

START:
    ; Тест команды LOAD с непосредственной адресацией
    LOAD #42         ; Загружаем константу 42
    STORE 100        ; Сохраняем в переменную A
    
    ; Тест команды LOAD с прямой адресацией
    LOAD 100         ; Загружаем переменную A (42)
    STORE 101        ; Сохраняем в переменную B
    
    ; Тест арифметических команд
    LOAD #10         ; Загружаем 10
    ADD #5           ; Прибавляем 5 (результат: 15)
    SUB #3           ; Вычитаем 3 (результат: 12)
    STORE 102        ; Сохраняем результат
    
    ; Тест команды сравнения
    LOAD 102         ; Загружаем результат (12)
    CMP #12          ; Сравниваем с 12
    JZ EQUAL         ; Если равно, переходим к EQUAL
    
    ; Этот код не должен выполниться
    LOAD #999
    STORE 103
    
EQUAL:
    ; Тест условного перехода
    LOAD #5          ; Загружаем 5
    CMP #10          ; Сравниваем с 10
    JZ END           ; Если равно, переходим к END (не должно выполниться)
    
    ; Этот код должен выполниться
    LOAD #777        ; Загружаем 777
    STORE 104        ; Сохраняем как признак правильного выполнения
    
END:
    HALT

; Ожидаемые результаты:
; Memory[100] = 42  (переменная A)
; Memory[101] = 42  (переменная B)
; Memory[102] = 12  (результат арифметики)
; Memory[103] = 0   (не должно быть изменено)
; Memory[104] = 777 (признак правильного выполнения)
