; Простая свертка без меток для GUI
; A = [2, 3] и B = [4, 5]
; Результат: 2*4 + 3*5 = 8 + 15 = 23

; Инициализация суммы
LOAD #0
STORE 100

; Первое произведение: A[0] * B[0] = 2 * 4 = 8
LOAD 200        ; A[0] = 2
STORE 101       ; Сохраняем A[0]
LOAD 210        ; B[0] = 4  
STORE 102       ; Сохраняем B[0]

; Умножение 2 * 4 = 8 (4 раза прибавляем 2)
LOAD #0         ; product = 0
ADD 101         ; +2 (первый раз)
ADD 101         ; +2 (второй раз)
ADD 101         ; +2 (третий раз)
ADD 101         ; +2 (четвертый раз)
ADD 100         ; Добавляем к общей сумме
STORE 100       ; sum = 8

; Второе произведение: A[1] * B[1] = 3 * 5 = 15
LOAD 201        ; A[1] = 3
STORE 101       ; Сохраняем A[1]  
LOAD 211        ; B[1] = 5
STORE 102       ; Сохраняем B[1]

; Умножение 3 * 5 = 15 (5 раз прибавляем 3)
LOAD #0         ; product = 0
ADD 101         ; +3 (первый раз)
ADD 101         ; +3 (второй раз)
ADD 101         ; +3 (третий раз)
ADD 101         ; +3 (четвертый раз)
ADD 101         ; +3 (пятый раз)
ADD 100         ; Добавляем к общей сумме
STORE 100       ; sum = 8 + 15 = 23

HALT

; Ожидаемый результат:
; Memory[100] = 23 (2*4 + 3*5 = 8 + 15 = 23)
